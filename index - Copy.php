<h2> About Me</h2>
    <p> I am an anthropologist with expertise in modern Chinese culture and immigration in the US. I received my PhD in Anthropology, Temple University in 2011. </p>
    
    <p>Now I would like to apply my anthropological expertise to UX research that can greatly improve others' lives. Being a natural complainer who is always irritated by products with hard-to-use design, I would like to devote myself to get rid of frustration from users and bring delight to them through reshaping their experience of the product. </p>
    
    <P> Therefore, I am now enrolled in <a href="http://www.hcde.washington.edu/" target="_blank">Human Center Design and Engineering program at University of Washington </a> for my MS. My focus area is on UX research. However, as a visual anthropologist, I love design as well, and spent a lot of time learning how to design.</P>           
    
    <P> Right now I finally can find a way to combine both my knowledge in Anthropology and UX research to work on UX projects in courses I am taking at UW. </P>
    <P> For the aspect of UX design, right now I am interested in mainly web and mobile design. My short term goal is to design and develop my own mobile application by the end of the first year in my masters program. </P>
    <P> My long term goal is to bring delight to users on the product I work on. I believe that users have the right to enjoy the product without any frustration all the time.</P>
