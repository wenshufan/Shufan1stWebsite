    <br>
    <h3>The Research is the Critical Part of the Design</h3>
    <p>We were also required to write a paper to explain our design. I appreciated the fact that we had to write a paper since the effort of doing research for the design was shown in paper since it was hard to discover the hard research work in the prototype itself. I spent as much time in doing research as the design. </p>
