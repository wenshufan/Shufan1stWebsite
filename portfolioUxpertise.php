    <h3> Summary </h3>
    <p> <strong> What I did: </strong>
    <ul>
      <li>Created online survey</li>
      <li>Competitor analysis and literature review</li>
      <li>Designed signup pages in Axure</li>
      <li>Generated interview questions</li>
      <li>Interviewed potential users</li>
    </ul>
    <br>
    <h3>A Solution to Enhance Mentorship in UX Industry</h3>
    <img class="headshot" src="Images/Projects/Uxpertise/u31.png" alt="Our team discuss the initial design"/>
    <p>First, I created a survey, and my teammates finalized it. We asked if people in UX field find there is a need to be involved in mentorship in UX area.</p>
    <img class="align-right" src="Images/Projects/Uxpertise/u35.png" alt="A picture of the survey."/>
    <p>Second, we did an interview of 6 potential mentors and 6 potential mentees in UX field. Throughout the interview process, we found critical features the website must have: connecting mentors and mentees, and maintaining the mentoring relationship.</p>
    <br>
    <br>
    <h3> User Research, Lit Review, and Competitive Analysis </h3>
    <p> In order to understand how to promote effective mentorship, and how it should be maintained on our online solution, we went through Computer Mediated Communication researches about online community. </p>
    <p>Moreover, I participated UW Women in Engineering Mentorship program to observe how a successful mentoring relationship should be initiated and maintained. During the process I talked to people there to gather info about ideal relationship between mentors and mentees.</p>
    <img class="align-right" src="Images/Projects/Uxpertise/u63.png" alt="A pic took in Women in Engineering mentorship meeting."/>
    <p> Then I created a registration process to match mentors and mentees. It was one of the critical feature on our website. Since the matching mechanism of mentoring relationship was similar to dating in psychological aspect, I did competitive analysis on websites such as match.com and harmony.com. I realized that these sites required users to go through a complicated registration process (about 20minutes) in order to view other usersâ€™ profiles. Interestingly, no scientific evidence available in lit review to show if their approaches were effective. I believed their complicated registration process was to filter out users who were not serious in using the website.</p>
    <img class="align-right" src="Images/Projects/Uxpertise/u65.jpg" alt="Screenshot of homepage of Match.com."/> <br>
    <h3> Preliminary Design </h3>
    <p> After discussing with my group members, we decided to employ the registration process before users can view complete profiles of others. However, my next task was to provide a 10 minutes registration process to create a brief but effective profile for automatic and manual match up mechanism. </p>
    <p>After all these research, I did the initial design In Axure to show my idea of how the profile should look like after successfully registered as a user. </p>
    <img class="large" src="Images/Projects/Uxpertise/u79.png" alt="My very first design for the registration page."/> <br>
    <h3> Ideation</h3>
    <p>After gathering all data needed, we did affinity diagram to find out which feature was critical, which ones were nice to have.</p>
    <img class="align-right" src="Images/Projects/Uxpertise/u93.png" alt="We are doing infinity diagram."/> <br>
    <br>
    <h3>Design Prototype: First Version</h3>
    <p>I worked with Zening to finish the registration pages. We picked up critical questions for the registration process according to psychology and CMC theories. To us, this process was more like doing a research in the beginning. Our first version was not very user friendly. </p>
    <p> Here is my first version of the registration process design: </p>
    <img class="large" src="Images/Projects/Uxpertise/u105.png" alt="First version of the registration process design."/> <br>
    <h3>Design Prototype: Second Verison</h3>
    <p>After a series of discussion, we decide to use slide bars instead of numbers. It is easier for users to make their decision in slide bars.</p>
    <img class="large" src="Images/Projects/Uxpertise/u124.png" alt="First version of registration process design."/>
    <p>We finally came up with the final version of design. We fixed several design flaws and wording problems.</p>
    <p> Here is the structure of our site: </p>
