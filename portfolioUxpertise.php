    <h2>UXpertise: the Ux Mentoship Website</h2>
    <p> <a href="http://students.washington.edu/susanwen/0pdffiles/UXpertiseFinalPaper.pdf" target="_blank"> <strong> View the paper about our website design here </strong></a></p>
    <p> <a href="http://6kpvc8.axshare.com/" target="_blank"> <strong> View the prototype </strong> <img class="large" src="Images/Projects/Uxpertise/u135.jpg" alt="screenshot of Uxpertise homepage"/></a> </p>
