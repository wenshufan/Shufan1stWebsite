	<h2>Portfolio: Google Cloud Storage Usability Testing </h2>
     <img class="headshot" src="Images/Projects/UsabilityTesting/u29.jpg" alt="Shufan with her teammates."/>
     
    <p><strong> In Winter 2014, </strong> our client, Mr. T, a Google UX team manager, would like to know how developers feel about their product, especially when comparing with other product these developers used. Qualitative information was needed about the basic function of using Google Cloud Storage. </p>
    
   	<br>
    <br>
    <br>
    
    <h3> The Most Challenging Project in Class? </h3>
    <p> Vina and I were the first two girls who picked up this project. We were trying to persuade boys who worked as Microsoft engineers to join us since we needed 2 more people. I remembered one engineer looked at us in funny face and said, "No, thanks. This project is too challenging even for a developer like me. Girls, I would suggest you to run away from it..." </p> 
    
    <img class="align-right" src="Images/Projects/UsabilityTesting/u57.png" alt="Our Google Storage Usability report cover!"/>
    
    <p>What? No way! I love challenges! Challenges make life fun and make me grow, even though there are price to pay.</p>
    <p>Later on, I knew what he meant. The scope was huge and the testing was complicated.  Well, we broke it into parts, and planned to solve everything step by step.</p>
    
    <br>
    <br>
    <h3> Our Plan for this Challenging Project </h3>
    <br>
    <p><a herf="http://students.washington.edu/susanwen/0pdffiles/5GTestPlan.pdf"> Click here to see our plan. </a> </p>
    
    <p>In order to finish this project in time, we made a plan.  </p>
    <p>First of all, we made sure we understand the product, and did a preliminary competitive study on the similarity and differences between Google Cloud Storage, Amazon S3, and Azure.</p>
    
    <p>It is a relatively complicated product to test since we only have 5 weeks to finish it. After discussion, I made a timeline for our project, and make sure we start early and have enough time for every process. </p>
     
    <p> We developed a test plan and scripts for moderators. Test plan included the test of basic functions for using Google Cloud Storage, including both web UI and command line tools. System Usability Scale (SUS) was added in the end to evaluate these tools. I carried out several pilot testings, and realized the total test time will be 2 hours for each participant.</p>
    
    <h3> Recruiting </h3>
    <p>One of the challenges we faced was recruiting. Since our stakeholder’s targeted users are developers, we had to recruit developers for our testing. Developers were usually paid more than $40 an hour, our compensation of $10 Starbucks cards and Google swags did not really work out well when asking them to do a 2 hour testing. Initially we were rejected even by our classmates.</p>
    
    <img class="large" src="Images/Projects/UsabilityTesting/u73.jpg" alt="My doodle about recruiting developers."/>
    <p><sup>Doodled by Shufan Wen</sup></p>
    
  	<p>We finally recruited 10 people by using our connections, Linkedin network, and personal resources in one week. I truly appreciate those participants who spent at least 2 hours in our test. Some of them spent 3 hours or more to finish our testings!</p>
	
    <img class="align-right" src="Images/Projects/UsabilityTesting/Toby.png" alt="Our dev friend was doing our testing!"/>
    
    <p>Lesson learned, you should never assume users are like you! I found developers difference species from me. They loved our long task list, and could not stop themselves from doing it. Some of them failed to finish tasks in 4 hours extended testing time, then they spent another several hours finished it at home and reported their thoughts to us! I truly appreciate their help! I was very excited because I realized that we discovered many significant issues to report. These issues were very interesting and could help to improve the product that we tested. </p>

    <img class="large" src="Images/Projects/UsabilityTesting/u91.jpg" alt="My doodle about terrific developers who spent 2hrs on our testing."/>
    <p><sup>Doodled by Shufan Wen</sup></p>
    <br>
   
    <h3> Data Analysis </h3> 
    <p>Then we did affinity diagram on the white board to determine problems that needed to be fixed, and rated the priority to problems. It took us a whole afternoon and evening since we had a huge amount of data. Even our dogs (who were waiting for us) were very tired in the end.</p>
    
	<img class="align-right" src="Images/Projects/UsabilityTesting/u109.jpg" alt="A hudge whiteboard with countless sticky notes for infinity diagram."/>
    
    <p> Lesson learned: We had to consider many aspects when designing an experiment. A good communication with stakeholders was critical in our experiment process. </p>
    
    <img class="large" src="Images/Projects/UsabilityTesting/u107.jpg" alt="Our dogs went to sleep since we spent too much time doing data analysis."/>
    
    <br>
    
    <h3> Class Presentation </h3>
    
    <p> <a herf="http://students.washington.edu/susanwen/0pdffiles/Finalppt.pdf"><strong> Click here to view the presentation slides </strong> </a></p>
    
    <p> We presented our findings in our class in the end of the quarter. When comparing to our classmates’ usability tests, we realized that the scope of our test was huge. However, we managed to sort out the most critical items for report and report the most critical findings only owing to the limited time and course requirement. Right now we are communicating with our client to schedule a time for presentation. </p>
    
    <img class="large" src="Images/Projects/UsabilityTesting/u125.jpg" alt="One critical slide from our presentation."/>
    
    <br>
    
    <h3> What I Learned </h3>
    
    <p>This project was extremely challenging for people without computer science background like me. Therefore, self-learning ability was extremely important when dealing with complicated project like this one. I had to learn the process and reprogram my thoughts like a developer in 2 weeks. Moreover, the ability of being able to do a usability testing without understanding every detail of the product was critical for this project. One had to see the big scope of it instead of the details within the test in order to successfully carry out the testing. I truly appreciated this learning opportunity and enjoyed this challenging project.</p>
    <img class="large" src="Images/Projects/UsabilityTesting/u135.jpg" alt="Our lovely dogs."/>
    
	<p>Thank you for reading! Hope you enjoy the picture: tired dogs with sticky notes from affinity diagram.</p>

