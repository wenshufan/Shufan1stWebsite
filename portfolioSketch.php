    <h3> Rearrange Alberson Supermarket in Eastgate</h3>
    <p>I usually go to this supermarket to grab things and go. However, my shopping experience was usually not very good. Usually I went in a hurry, and always forgot to grab a basket with me. Later on when I realized that I need a basket, I had to march out the front door to get one. Most of the time I just put down things I was going to buy instead. Moreover, they sometimes had intimidating donation stands at the entrance. I don't feel comfortable to pass by them when I was in a hurry, and would choose to go to the drugstore next to it if things I need was available there. </p>
    <img class="large" src="Images/Projects/Sketches/u49.jpg" alt="Original arrangement of Alberson Supermarket in Eastgate"/>
    <p> In my re-design of the market, I put baskets at the lower row in the end of almost every shelf. I noticed that the lowest shelf was not efficiently used anyway (which was usually empty), why don't they put some baskets for shoppers' convenience?</p>
    <img class="large" src="Images/Projects/Sketches/u51.jpg" alt="My first re-arrangement of Alberson Supermarket in Eastgate sketch"/> <br>
    <p>The design of the Alberson Supermarket was strange to me. Unlike most supermarket, it did not have additional shelves to display merchandise along the checkout counter. I loved to read gossip magazines when waiting in checkout line, but it was not the option in this Alberson. Probably the supermarket was too small for having a magazine shelf at checkout area. My suggestion was to add something interesting or "intimidating" ( such as the donation stand) so people could be prompted to buy things or donate at the last minute when they were bored by waiting in line for checking out.</p>
    <img class="large" src="Images/Projects/Sketches/u53.jpg" alt="My second re-arrangement of Alberson Supermarket in Eastgate sketch"/>
    <p>Sometimes I went to Alberson just to grab a DVD from Redbox. They had Redbox machine conveniently set right next to the entrance. So I grabbed the DVD and went home, and even forgot that I needed to get yogurt too. If it was possible (probably Redbox would not like it), they could move Redbox machine to the middle of the store where people had to pass by something interesting (such as on sale goods or cosmetics) so customers might look at it and buy additional things.</p>
    <img class="large" src="Images/Projects/Sketches/u55.jpg" alt="My third re-arrangement of Alberson Supermarket in Eastgate sketch"/>
    <p>But I wonder if people will complain that they have to walk a little bit to reach Redbox in my design since sometimes people can be lazy :p </p>
