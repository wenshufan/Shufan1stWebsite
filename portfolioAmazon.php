  <h2>Portfolio: Amazon Web Service Internship </h2>
  <img class="headshot" src="Images/Projects/AWS/AWS2.jpg" alt="Shufan with Amazon box man"/>
  <p><strong> In Summer 2014</strong> I worked as a User Research Intern under AWS Console Team at AWS. It was a wonderful experience because I had a chance to be an independent user researcher under my mentor, Andrew, and manager, Jens', help. I had the chance to cooperate with fantastic UX designers for design iterations, work with the project manager to find out how to solve usability problem, and discuss with developers about possible/practical solutions. </p>
  <br>
  <br>
  <h3> Gained the real experience as a sole researcher in real projects </h3>
  <p> I completed three projects and brought insights to design iteration. UX designers and project managers told me my reports were very helpful to them. In the end of my internship, I had the honor to present to the head of a service console and a team of developers, and my presentation was broadcasted to East coast Amazon locations in real time. </p>
  <img class="align-right" src="Images/Projects/AWS/AWS1.jpg" alt="Shufan in the front of scrum board."/>
  <p> I really enjoyed the UX research process! During the internship, after meeting with stakeholder and understood their needs, I generated the research questions according to stakeholders' needs, recruited users, arranged lab equipment, carried out usability study, analyzed data gathered, generated report, and presented to stakeholders. Sometimes I use Axure to generate prototype quickly for testing during the process, and realized what I learned in HCDE is very useful. In some projects, I had the honor to work with UX designers for iteration and design decisions. In one project, I worked like a project manager, took the responsibility of the product, and held up meetings to drive the progress</p>
  <br>
  <h3> This internship taught me aspects I should work on in order to be a great UX professional </h3>
  <p> During the internship, I found developers' logic is different from UX professionals! Even though I am married to a developer and have a lot of developer friends, I had no idea what they do and could not understand their chats about work. Then I realized that I had very limited understanding about computational concepts to talk to them in "developers' language". As an anthropologist, in order to understand a certain kind of people, you have to "live their life". It is the main reason why I decided to get my hands dirty by developing Mobile Application by using C# in Autumn 2014. I gained a great insights on how they think and their development environment, and found myself be able to talk to my developer friends much easily. Please see My UX Phone App for more information.</p>
