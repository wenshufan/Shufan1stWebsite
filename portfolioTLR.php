  <h3> The Story </h3>
  <p><strong>In autumn 2013</strong> In HCDE 518 User Center Design course, the instructor asked us to do a project "for good".</p>
  <p>Our group decided to design a solution for earthquake awareness and preparation. Since Seattle was located at earthquake-prone area, we all believed it was important for people to prepare for possible major earthquakes. We were going to create an easy solution for earthquake preparation and raise earthquake awareness.</p>
  <p>We called ourselves--The Last Responders! There are 5 people in my group: Mike, Pratham, Eric, Ruben, and me.</p>
  
  <p>We did not assign any roles for our group member, but everyone seemed to take their roles naturally according what they do well. Since I was an anthropologist, I carried out the research part in our project naturally.</p>
  <p>Also I was the only femal in our group who always argued that gender element was important in our project. </p>
  <br>
  <h3> Preliminary Sketches </h3>
  <p>I was very excited about this project, so I started to sketch the imaginary product we would have after the quarter. My ideal produict was a fun application on mobile which people could check out and play anytime and anywhere.</p>
  <img class="large" src="Images/Projects/TLR/u35-4.jpg" alt="Shufan's sketches for earthquake app."/>
  <p>I imagined the gamification test of prepareness will make our application stand out. I believed that users would like the app and learn about how to prepare for the earthquake through the game. </p>
  <img class="large" src="Images/Projects/TLR/u37-4.jpg" alt="Shufan's sketches for earthquake app."/>
<br>
<h3> Literature Review & Competitive Analysis</h3>
<p>In the beginning, we were not sure what the solution was. It could be a website, a campaign, a poster, or even an application. </p>
<p>We started with a literature review on information available in the library and online. We hoped to find out the definition of "being prepared", and how prepared people were in their previous research.</p>
<img class="align-right" src="Images/Projects/TLR/u53.png" alt="A picture of Seattle.gov."/>
<p>As a researcher, I looked at FEMA websites, and Seattle gov ones. and summarized my findings about earthquake, and what things are essential to know for preparation.</p>
<p>In our group meeting, we took time to define preparedness of an earthquake. We found most government websites provided detailed earthquake information, but not easy steps of preparation. </p>
<br>
<h3>User Research</h3>
<p>From our research information, we decided to use FEMA standard for earthquake preparation to develop survey questions toward our targeted users—UW students. </p>
<img class="align-right" src="Images/Projects/TLR/u69-1.jpg" alt="Shufan was interviewing informants."/><sub>Picture: Shufan was interviewing a family about their earthquake preparation.</sub>
<p>We also decided to do interviews and contextual inquiries to get qualitative data about our potential users.</p>
<p>Since I am an anthropologist by nature, I collected most of the qualitative data and did analysis. I interviewed five people (out of six total) and did one contextual inquiry. I presented my findings in summaries and provided design directions in our meeting.</p>
<br>
<br>
<h3>Data Analysis & Ideation</h3>
<p>After we collected all data, we started the analysis on our data. We realized that there are primary three types of users who we were targeting. We draft the idea down on a graph according to preparedness and motivation.</p>
<p>We created 3 personas. There were one primary and two secondary according to this scale.</p>
<img class="align-right" src="Images/Projects/TLR/u83.png" alt="Our evaluation scale."/>
<br>
<h3>Personas</h3>
<h4> The Primary Persona</h4>
<p>Typical Tommy: the UW student who did not have motivation and does not want to spend a lot time to prepare for the earthquake. From our survey and interview data, the majority full-time UW students belonged to this category (47% in our survey).</p>
<img class="large" src="Images/Projects/TLR/u97.jpg" alt="Typical Tommy Persona"/>
<br>
<h4> The Secondary Persona</h4>
<p>I initialized this idea to create this persona. The secondary persona was created from my interview results with families. In literature review, FEMA suggested people who had families tend to have more motivation to prepare for an earthquake, but they did not have time to do it. </p>
<p>Our survey and interview result suggested so. I did t-test analysis on our survey data to compare the motivation for preparation of earthquakes between people who had children and who did't. There was a significant difference in motivation between people who had children and who did't (p=0.01). Moreover, my interview results shown that parents were highly motivated to prepare for earthquakes mainly for their children and properties.</p>
<p>Therefore, I proposed to my team that we should target this group of users since they had high motivation for preparation. Therefore, we created Busy Barbara as our secondary persona. Most UW students who had children fall in this category.</p>
<img class="large" src="Images/Projects/TLR/u111.jpg" alt="Busy Barbara Persona."/>
<br>
<h4>The Other Secondary Persona</h4>
<p>The other secondary persona was Prepared Peter. There were only a few people in our survey and interview belonged to this persona (only 1 out of 6). </p>
<p>However, they have high motivation and were prepared for the earthquake in certain degree. They would like to know more about earthquake preparation and would like to have updated information. Our team hoped we can provide useful info for this group of users as well as the previous two.</p>
<img class="large" src="Images/Projects/TLR/u123.jpg" alt="Prepared Peter Persona."/>
<br>
<br>
<h3>Design Map</h3>
<p>After creating personas, we created a design map so we can visualize what we were going to build for the solution. From our interview and survey results, most users prefered to have a website for the earthquake preparation solution. </p>
<p>We figured out what the user would do by building up a design map.</p>
<img class="align-right" src="Images/Projects/TLR/u139.jpg" alt="Our design map sticky notes."/>
<br>
<br>
<h3>Ideation</h3>
<p>Our team found it was extremely difficult to sort out so much information provided by government emergency response websites into simple steps of solution we could use. We spent a great amount of time on ideation of our website. Each of us sketched down our ideas and explained to our team members on how to present complicated information into simple and clear presentation.</p>
<img class="align-left" src="Images/Projects/TLR/u155.png" alt="Shufan go through sketches."/>
<br>
<br>
<h3>Sketches of Initial Design</h3>
<p>Each of us did three sketches about our ideas of the website. We looked at each others’ sketches, and gave critique on them.</p>
<p>In my sketches, I design websites with big clickable buttons because personally I havd close relationship with vision impaired community and would like to design websites that were easy for them to use.</p> 
<p>Sketch 1 </p>
<p>Since my informants said they will be motivated if there were freebies to get, I designed my homepage with the emphasis of "FREE KIT" available.</p>
<img class="large" src="Images/Projects/TLR/u173-1.jpg" alt="My sketch of the webdesign with freebies"/>
<p>Sketch2:</p>
<p>I redesign the homepage with motivation facts: exploding mountain St. Helens, video of crying victims from 2001 SeaTac earthquake. Three easy buttons for preparation according to our personas on the left, and personalized kit in the bottom.</p>
<p>My team liked my idea to have scary facts displayed at the homepage which served the function to motivate people.</p>
<img class="large" src="Images/Projects/TLR/u175-1.jpg" alt="My sketch of the webdesign with scarry facts"/>
<br>
<br>
<h3>The Paper Prototype</h3>
<p>After sketches and boiling down our ideas into simple steps, our team decided to do a paper prototype. Pratham and Eric spent their whole afternoon and created a paper prototype to create their experiment of the low-fidelity prototype.</p>
<img class="align-left" src="Images/Projects/TLR/u290.jpg" alt="The paper prototype."/>
<p>However, when we did our pilot usability test, we found that participants could not figure out what was happening in the very low fidelity paper prototype. Our team immediately decided to start to design medium-high fidelity prototype by using Axure. Since we only have less than five days to arranged usability testing, Mike, Eric, and Pratham worked through the weekend to make it possible for me to start testing on Monday.</p>
<img class="align-right" src="Images/Projects/TLR/u292.jpg" alt="Test the paper prototype."/>
<br>
<br>
<h3>Test The First Version of our Axure Prototype</h3>
<p>My teammates created the first version of our Axure Prototype, and I did all the usability testing by using this prototype. </p>
<img class="align-right" src="Images/Projects/TLR/u210.jpg" alt="Homepage of the 1st version prototype."/>
<p>The next step was usability testing. It was very natural that I took the role as the moderator for usability testings. I started arranging participants for testing about a week ago when we had paper prototype.  Eric helped me revised the script, Pratham was the assistant during usability test. Originally we would like to carry out two rounds of usability testing to iteration, but we could not do it owing to the limited amount of time we had.</p>
<img class="align-right" src="Images/Projects/TLR/u234.jpg" alt="Test with Zening."/>
<p>Therefore, I did usability testing with three participants for one round only. Since I did not participate in the physical design of the Axure prototype, I was very open to users’ reaction and criticism, and was able to provide a neutral feedback in my report back to our design team. I enjoyed being a moderator and really appreciate the fact that my team gave me the opportunity to allow me taking the role that I feel most comfortable to.</p>
<p>Moreover, I would like to thank my participants (Zening, Chia-Chen and Mary) for participating our test which was 50 minutes long. They provide useful feedbacks which allowed our team to take actionable resolution to solve usability problems they encountered.</p>
<img class="large" src="Images/Projects/TLR/u232.png" alt="Testing screenshot of Zening"/>
<p>During the testing, I recorded the process by using Microsoft Expression Encorder. Our team observed the video at our meeting during my report. It was a great way to observe user's facial expression with their browsing behavior.</p>

<br>
