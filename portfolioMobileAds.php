    <h2>Mobile Ads Experimental Research:<br>
      Best Location and Optimized Size for Ads on a Photo-based Mobile App</h2>
    <div class="shadowbox">
      <h3> Summary </h3>
      <p> <strong> Goal:</strong>Design an experimental research to find out which type/location of advertisement is effective without decreasing customer satisfaction too much.</p>
      <p><strong> Problem:</strong>Evaluate the relationship between type/location of the ads and customer satisfaction</p>
      <p><strong> Deliverable: </strong> A report with experimental lab research plan and effective statistical methodologies for analysis</p>
      <p><strong> My Role:</strong>Project Manager</p>
      <p> <strong> What I did: </strong></p>
      <ul>
        <li>Literature review</li>
