        <li>Simplified fuzzy questions into actionable hypothesis</li>
        <li>Designed the within-subject experiment</li>
        <li>Determined statistical methods for data analysis </li>
        <li>Manage to deliver progress report and presentation each weak</li>
        <li>Experimental research process design</li>
        <li>Writing up and finalize the report</li>
      </ul>
